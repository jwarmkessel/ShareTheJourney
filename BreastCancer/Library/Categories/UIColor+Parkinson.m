#import "UIColor+Parkinson.h"

@implementation UIColor (Parkinson)

+ (UIColor *)parkinsonLightGrayColor
{
    return [UIColor colorWithRed:215/255.f green:215/255.f blue:215/255.f alpha:1.0];
}

+ (UIColor *)parkinsonDarkGrayColor
{
    return [UIColor colorWithRed:127/255.f green:127/255.f blue:127/255.f alpha:1.0];
}

+ (UIColor *)parkinsonBlueColor
{
    return [UIColor colorWithRed:29/255.f green:90/255.f blue:170/255.f alpha:1.0];
}

+ (UIColor *)parkinsonBlackColor
{
    return [UIColor colorWithRed:40/255.f green:40/255.f blue:40/255.f alpha:1.0];
}

+ (UIColor *)parkinsonGreenColor
{
    return [UIColor colorWithRed:48/255.f green:181/255.f blue:95/255.f alpha:1.0];
}


@end
